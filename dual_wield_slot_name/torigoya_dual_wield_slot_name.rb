module Torigoya
  module DualWieldSlotName
    # ● 二刀流の2本目の武器のスロット名
    SLOT_NAME = 'サブ武器'
  end
end

#===============================================================================
# ↑ 　 ここまで設定 　 ↑
# ↓ 以下、スクリプト部 ↓
#===============================================================================

class Window_EquipSlot < Window_Selectable
  #--------------------------------------------------------------------------
  # ● 装備スロットの名前を取得（エイリアス）
  #--------------------------------------------------------------------------
  alias torigoya_dual_wield_slot_name__slot_name slot_name
  def slot_name(index)
    return ::Torigoya::DualWieldSlotName::SLOT_NAME if @actor && @actor.equip_slots[index] == 0 && index != 0
    torigoya_dual_wield_slot_name__slot_name(index)
  end
end
